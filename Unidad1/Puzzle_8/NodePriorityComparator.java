package Puzzle_8;

import java.util.Comparator;

public class NodePriorityComparator implements Comparator<Nodo> {
 
    @Override
    public int compare(Nodo x, Nodo y) {
        if (x.costoAcumulado < y.costoAcumulado) {
            return -1;
        }
        if (x.costoAcumulado > y.costoAcumulado) {
            return 1;
        }
        return 0;
    }
}